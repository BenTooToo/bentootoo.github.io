const languageMap = {
  zh: "zh-CN",
  en: "en",
  fr: "fr"
};

const prefersReducedMotion = window.matchMedia("(prefers-reduced-motion: reduce)").matches;
const savedLanguage = localStorage.getItem("bentootoo-language");
const defaultLanguage = savedLanguage && translations[savedLanguage] ? savedLanguage : "zh";

function setLanguage(language) {
  const dictionary = translations[language] || translations.zh;

  document.documentElement.lang = languageMap[language] || "zh-CN";
  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    if (dictionary[key]) {
      element.textContent = dictionary[key];
    }
  });

  document.querySelectorAll("[data-i18n-aria]").forEach((element) => {
    const key = element.dataset.i18nAria;
    if (dictionary[key]) {
      element.setAttribute("aria-label", dictionary[key]);
    }
  });

  document.querySelectorAll(".lang-button").forEach((button) => {
    const isActive = button.dataset.lang === language;
    button.classList.toggle("is-active", isActive);
    button.setAttribute("aria-pressed", String(isActive));
  });

  localStorage.setItem("bentootoo-language", language);
}

document.querySelectorAll(".lang-button").forEach((button) => {
  button.addEventListener("click", () => setLanguage(button.dataset.lang));
});

setLanguage(defaultLanguage);

const year = document.querySelector("#year");
