const avatarCard = document.querySelector(".avatar-card");
if (avatarCard) {
  const toggleAvatarCard = () => {
    const isFlipped = avatarCard.classList.toggle("is-flipped");
    avatarCard.setAttribute("aria-pressed", String(isFlipped));
  };

  avatarCard.addEventListener("click", toggleAvatarCard);
  avatarCard.addEventListener("keydown", (event) => {
    if (event.key === "Enter" || event.key === " ") {
      event.preventDefault();
      toggleAvatarCard();
    }
  });

  avatarCard.querySelectorAll("a").forEach((link) => {
    link.addEventListener("click", (event) => event.stopPropagation());
  });
}

const moreMenu = document.querySelector(".more-menu");
const moreToggle = document.querySelector(".more-toggle");
const donateMenu = document.querySelector(".donate-menu");
const donateTrigger = document.querySelector(".donate-trigger");

if (moreMenu && moreToggle) {
  const setDonateSubmenuOpen = (isOpen) => {
    if (!donateMenu || !donateTrigger) {
      return;
    }

    donateMenu.classList.toggle("is-submenu-open", isOpen);
    donateTrigger.setAttribute("aria-expanded", String(isOpen));

    if (!isOpen) {
      donateTrigger.blur();
    }
  };

  const setMoreMenuOpen = (isOpen) => {
    moreMenu.classList.toggle("is-open", isOpen);
    moreToggle.setAttribute("aria-expanded", String(isOpen));

    if (!isOpen) {
      setDonateSubmenuOpen(false);
    }
  };

  moreToggle.addEventListener("click", (event) => {
    event.stopPropagation();
    setMoreMenuOpen(!moreMenu.classList.contains("is-open"));
  });

  moreMenu.querySelectorAll(".more-dropdown > a, .donate-submenu a").forEach((control) => {
    control.addEventListener("click", () => setMoreMenuOpen(false));
  });

  if (donateMenu && donateTrigger) {
    donateTrigger.addEventListener("click", (event) => {
      event.stopPropagation();
      setDonateSubmenuOpen(!donateMenu.classList.contains("is-submenu-open"));
    });
  }

  document.addEventListener("click", (event) => {
    if (!moreMenu.contains(event.target)) {
      setMoreMenuOpen(false);
    } else if (donateMenu && !donateMenu.contains(event.target)) {
      setDonateSubmenuOpen(false);
    }
  });

  document.addEventListener("keydown", (event) => {
    if (event.key === "Escape") {
      setMoreMenuOpen(false);
      setDonateSubmenuOpen(false);
    }
  });
}

const revealObserver = new IntersectionObserver(
  (entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        entry.target.classList.add("is-visible");
        revealObserver.unobserve(entry.target);
      }
    });
  },
  {
    threshold: 0.18,
    rootMargin: "0px 0px -8% 0px"
  }
);

document.querySelectorAll("[data-reveal]").forEach((element) => revealObserver.observe(element));

function updateProgress() {
  const scrollable = document.documentElement.scrollHeight - window.innerHeight;
  const progress = scrollable > 0 ? (window.scrollY / scrollable) * 100 : 0;
  document.documentElement.style.setProperty("--progress", `${Math.min(progress, 100)}%`);
}

const siteHeader = document.querySelector(".site-header");
const backToTop = document.querySelector(".back-to-top");
let lastScrollY = window.scrollY;

function updateHeaderVisibility() {
  if (!siteHeader) {
    return;
  }

  const currentScrollY = window.scrollY;
  const scrollingDown = currentScrollY > lastScrollY;
  const isNearTop = currentScrollY < 80;
  const changedEnough = Math.abs(currentScrollY - lastScrollY) > 4;

  if (isNearTop) {
    siteHeader.classList.remove("is-hidden");
  } else if (changedEnough && scrollingDown) {
    siteHeader.classList.add("is-hidden");
  } else if (changedEnough) {
    siteHeader.classList.remove("is-hidden");
  }

  lastScrollY = Math.max(currentScrollY, 0);
}

function updateBackToTopVisibility() {
  if (!backToTop) {
    return;
  }

  const distanceFromBottom = document.documentElement.scrollHeight - window.innerHeight - window.scrollY;
  backToTop.classList.toggle("is-visible", distanceFromBottom < 180);
}

window.addEventListener("scroll", () => {
  updateProgress();
  updateHeaderVisibility();
  updateBackToTopVisibility();
}, { passive: true });
window.addEventListener("resize", () => {
  updateProgress();
  updateBackToTopVisibility();
});
updateProgress();
updateHeaderVisibility();
updateBackToTopVisibility();

const canvas = document.querySelector("#heroCanvas");
const context = canvas ? canvas.getContext("2d") : null;
const pointer = { x: 0.5, y: 0.5 };

if (canvas && context && !prefersReducedMotion) {
  let width = 0;
  let height = 0;
  let dpr = 1;
  let time = 0;

  function resizeCanvas() {
    dpr = Math.min(window.devicePixelRatio || 1, 2);
    width = canvas.clientWidth;
    height = canvas.clientHeight;
    canvas.width = Math.floor(width * dpr);
    canvas.height = Math.floor(height * dpr);
    context.setTransform(dpr, 0, 0, dpr, 0, 0);
  }

  function drawRibbon(offset, color, amplitude, speed) {
    context.beginPath();
    const influenceX = (pointer.x - 0.5) * 90;
    const influenceY = (pointer.y - 0.5) * 70;

    for (let x = -40; x <= width + 40; x += 18) {
      const normalized = x / Math.max(width, 1);
      const waveOne = Math.sin(normalized * 8 + time * speed + offset) * amplitude;
      const waveTwo = Math.cos(normalized * 15 - time * speed * 0.7 + offset) * (amplitude * 0.34);
      const y = height * 0.5 + waveOne + waveTwo + influenceY + Math.sin(time * 0.6 + offset) * 34;
      const finalX = x + influenceX * Math.sin(normalized * Math.PI);

      if (x === -40) {
        context.moveTo(finalX, y);
      } else {
        context.lineTo(finalX, y);
      }
    }

    context.strokeStyle = color;
    context.lineWidth = 1.7;
    context.stroke();
  }

  function drawMesh() {
    context.clearRect(0, 0, width, height);
    context.globalCompositeOperation = "source-over";
    context.fillStyle = "rgba(16, 17, 15, 0.16)";
    context.fillRect(0, 0, width, height);

    context.globalCompositeOperation = "lighter";
    drawRibbon(0, "rgba(25, 211, 197, 0.48)", 92, 0.72);
    drawRibbon(1.8, "rgba(200, 255, 89, 0.36)", 72, 0.58);
    drawRibbon(3.4, "rgba(255, 106, 87, 0.3)", 112, 0.46);

    context.globalCompositeOperation = "source-over";
    context.strokeStyle = "rgba(246, 247, 242, 0.08)";
    context.lineWidth = 1;

    const columns = 9;
    const rows = 5;
    for (let col = 0; col <= columns; col += 1) {
      const x = (width / columns) * col + Math.sin(time * 0.35 + col) * 10;
      context.beginPath();
      context.moveTo(x, 0);
      context.lineTo(x + Math.sin(time * 0.5 + col) * 18, height);
      context.stroke();
    }

    for (let row = 0; row <= rows; row += 1) {
      const y = (height / rows) * row + Math.cos(time * 0.42 + row) * 8;
      context.beginPath();
      context.moveTo(0, y);
      context.lineTo(width, y + Math.cos(time * 0.3 + row) * 18);
      context.stroke();
    }
  }

  function tick() {
    time += 0.016;
    drawMesh();
    requestAnimationFrame(tick);
  }

  window.addEventListener("resize", resizeCanvas);
  window.addEventListener("pointermove", (event) => {
    pointer.x = event.clientX / Math.max(window.innerWidth, 1);
    pointer.y = event.clientY / Math.max(window.innerHeight, 1);
  });

  resizeCanvas();
  tick();
} else if (canvas && context) {
  canvas.style.display = "none";
}
